public class MapperClass {

    private String desc;
    private String msg;

    public MapperClass(String desc, String msg) {
        this.desc = desc;
        this.msg = msg;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
